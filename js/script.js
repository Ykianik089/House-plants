function highlightSearchResults() {
    const searchValue = document.getElementById("search").value.trim().toLowerCase();
    const elements = document.querySelectorAll("section-header, block"); // Выбираем нужные элементы
    let found = false; // Флаг для проверки, были ли найдены совпадения

    // Если строка поиска пуста, скрываем сообщение и выходим
    if (!searchValue) {
        document.getElementById("no-results-message").style.display = "none"; // Скрыть сообщение, если поиск пустой
        return;
    }

    // Проверка каждого элемента на наличие текста, соответствующего поисковому запросу
    elements.forEach(element => {
        const elementText = element.textContent.trim().toLowerCase();
        if (elementText.includes(searchValue)) {
            found = true; // Устанавливаем флаг, если есть совпадение
        }
    });
}


function scrollToSearchResult() {
    const searchValue = document.getElementById("search").value.trim().toLowerCase();
    if (!searchValue) return;

    // 1. Сначала ищем среди заголовков разделов (Аа, Бб, ... Яя)
    const sectionHeaders = document.querySelectorAll(".section-header");
    for (let header of sectionHeaders) {
        if (header.textContent.trim().toLowerCase() === searchValue) {
            header.scrollIntoView({ behavior: "smooth", block: "start" });
            return;
        }
    }

    // 2. Если не нашли в заголовках, ищем среди названий растений
    const plantBlocks = document.querySelectorAll(".block");
    for (let block of plantBlocks) {
        const plantName = block.querySelector(".block-text").textContent.trim().toLowerCase();
        if (plantName.includes(searchValue)) {
            block.scrollIntoView({ behavior: "smooth", block: "center" });
            return;
        }
    }
    alert("Ничего не найдено. Неправильный ввод, либо такого растения здесь нет");
}



