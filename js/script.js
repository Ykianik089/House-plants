function applyFadeOutOnScroll() {
    const intro = document.getElementById('intro');
    const scrollPosition = window.scrollY;

    if (scrollPosition > 225) {
        intro.classList.add('fade-out');
    } else {
        intro.classList.remove('fade-out');
    }
}

window.addEventListener('scroll', applyFadeOutOnScroll);


function searchSections() {
    const input = document.getElementById('search').value.toLowerCase();
    const sections = document.querySelectorAll('section');

    sections.forEach(section => {
        const sectionHeader = section.querySelector('.section-header').textContent.toLowerCase();
        if (sectionHeader.includes(input)) {
            section.style.display = 'block';
            section.scrollIntoView({ behavior: 'smooth' });
        } else {
            section.style.display = 'none';
        }
    });
}


